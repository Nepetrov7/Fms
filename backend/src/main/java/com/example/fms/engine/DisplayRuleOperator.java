package com.example.fms.engine;

/**
 * Операторы сравнения, которые используются в правилах отображения задач.
 *
 * <p>Строковые и логические параметры поддерживают только {@link #EQ} и
 * {@link #NE}. Числовые параметры, например количество дней с даты въезда,
 * поддерживают весь набор операторов.</p>
 */
public enum DisplayRuleOperator {
    /** Значение равно ожидаемому. */
    EQ,

    /** Значение не равно ожидаемому. */
    NE,

    /** Значение больше ожидаемого. */
    GT,

    /** Значение меньше ожидаемого. */
    LT,

    /** Значение больше или равно ожидаемому. */
    GTE,

    /** Значение меньше или равно ожидаемому. */
    LTE;

    /**
     * Преобразует строковый код из API или базы данных в оператор сравнения.
     *
     * <p>Пустое значение трактуется как {@link #EQ}, чтобы простые правила
     * могли не передавать оператор явно.</p>
     *
     * @param op строковый код оператора; регистр не важен
     * @return соответствующий оператор сравнения
     * @throws IllegalArgumentException если код непустой, но неизвестный
     */
    public static DisplayRuleOperator fromString(String op) {
        if (op == null || op.isBlank()) {
            return EQ;
        }
        return DisplayRuleOperator.valueOf(op.trim().toUpperCase());
    }
}
